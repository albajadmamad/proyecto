<h2>¿Qué es un recurso frontend y cuál es su función en el desarrollo web?</h2>
<br>
<p>El desarrollo front end es aquel que da una estructura a los datos que aparecen en una interfaz gráfica, con el fin de optimizar la experiencia del usuario. Es el encargado de definir la jerarquía de la información que se visualiza y su distribución: los márgenes, los colores, los recursos audiovisuales, las animaciones, etc. El resultado es lo que todos conocemos: una página web.

En contraposición a la programación back end, que se encarga del funcionamiento de las aplicaciones web del lado del servidor, el desarrollo front end está enfocado en la parte visible de los sitios, con la que interactúa el usuario. Aunque sería un error simplificar esta tecnología como un utensilio para cuidar la apariencia de una página.</p>
<br>
<br>
<h2>¿Cuáles son las principales tecnologías o herramientas utilizadas en el
desarrollo frontend y por qué son importantes?</h2>
<br>
<p>El desarrollo frontend se refiere a la creación de la parte de un sitio web o aplicación que los usuarios interactúan directamente. Aquí hay algunas de las principales tecnologías o herramientas utilizadas en el desarrollo frontend y por qué son importantes:

HTML (HyperText Markup Language): HTML es el lenguaje de marcado estándar para crear páginas web. Es fundamental en el desarrollo frontend ya que proporciona la estructura básica de una página web, permitiendo a los desarrolladores definir el contenido y la organización de la información.

CSS (Cascading Style Sheets): CSS se utiliza para definir el estilo visual de una página web, como el diseño, los colores, las fuentes y el espaciado. Es esencial para el desarrollo frontend porque permite a los desarrolladores controlar el aspecto y la presentación de una página web, lo que mejora la experiencia del usuario.

JavaScript: JavaScript es un lenguaje de programación que se utiliza para agregar interactividad y funcionalidad a las páginas web. Es importante en el desarrollo frontend porque permite a los desarrolladores crear efectos dinámicos, animaciones, validaciones de formularios, y comunicación con servidores, entre otras cosas.</p>
<br>
<h2>Describe el concepto de responsividad en el diseño web y menciona al
menos dos técnicas para lograrlo.</h2>
<br>
<p>La responsividad en el diseño web se refiere a la capacidad de una página web para adaptarse y mostrarse correctamente en una variedad de dispositivos y tamaños de pantalla, desde computadoras de escritorio hasta teléfonos móviles y tabletas. El objetivo es proporcionar una experiencia de usuario óptima sin importar el dispositivo que se esté utilizando. La responsividad implica ajustar el diseño, la disposición y el tamaño de los elementos de la página para que se vean y funcionen de manera adecuada en diferentes contextos.

Aquí están dos técnicas comunes para lograr la responsividad en el diseño web:

Media Queries: Las media queries son una característica de CSS que permite aplicar estilos específicos según las características del dispositivo, como el ancho de la pantalla, la altura, la orientación, la resolución, entre otros. Esto permite a los diseñadores y desarrolladores crear reglas de estilo condicionales que se aplican solo cuando se cumplen ciertos criterios. Por ejemplo, se pueden definir estilos diferentes para pantallas pequeñas, medianas y grandes, lo que permite adaptar el diseño y la disposición de los elementos según el tamaño del dispositivo.</p>
