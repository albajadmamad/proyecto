<h2>¿Cuál es la diferencia entre HTML, CSS y JavaScript en el contexto del
desarrollo frontend? Explique brevemente el papel de cada uno de estos
lenguajes.</h2>
<p>En el contexto del desarrollo frontend, HTML, CSS y JavaScript son tres lenguajes fundamentales que desempeñan roles distintos pero complementarios:

HTML (HyperText Markup Language):

HTML se utiliza para crear la estructura y el contenido de una página web.
Es un lenguaje de marcado que define los elementos y la organización de la información en una página, como encabezados, párrafos, imágenes, enlaces, formularios, etc.
HTML proporciona la base sobre la cual se construye una página web, determinando qué elementos se mostrarán y cómo se relacionarán entre sí.
CSS (Cascading Style Sheets):

CSS se encarga de definir el estilo visual y la presentación de una página web creada con HTML.
Permite establecer el diseño, los colores, las fuentes, el espaciado y otros aspectos visuales de los elementos HTML.
CSS separa la estructura del contenido (definido en HTML) de su presentación visual, lo que facilita la personalización y la modificación del aspecto de una página sin cambiar su estructura subyacente.
JavaScript:

JavaScript es un lenguaje de programación que se utiliza para agregar interactividad y funcionalidad dinámica a una página web.
Permite responder a eventos del usuario, manipular el contenido de la página en tiempo real, realizar validaciones de formularios, crear animaciones y efectos visuales, y comunicarse con servidores para cargar o enviar datos, entre otras cosas.
JavaScript complementa HTML y CSS al permitir que una página web sea más dinámica y receptiva a las acciones y necesidades del usuario.</p>
