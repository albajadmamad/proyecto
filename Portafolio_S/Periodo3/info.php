<h2>• Habla sobre la importancia de la accesibilidad en el diseño frontend y
menciona al menos dos prácticas para mejorar la accesibilidad en una página
web.</h2>
<br>
<p>
La accesibilidad en el diseño frontend es fundamental para garantizar que todas las personas, independientemente de sus capacidades o limitaciones, puedan acceder y utilizar una página web de manera efectiva. Al hacer que una página web sea accesible, se mejora la experiencia de usuario para una amplia gama de personas, incluidas aquellas con discapacidades visuales, auditivas, motoras o cognitivas. Aquí hay algunas razones clave por las cuales la accesibilidad es importante en el diseño frontend:

Equidad y Justicia: La accesibilidad garantiza que todas las personas tengan igualdad de oportunidades para acceder a la información y participar en la web, lo que promueve la equidad y la inclusión en línea.

Cumplimiento Legal y Normativo: En muchos países, existen leyes y regulaciones que exigen que los sitios web cumplan con ciertos estándares de accesibilidad para garantizar la igualdad de acceso para todas las personas. Cumplir con estos estándares no solo es una obligación legal, sino también una buena práctica ética.

Mejora de la Experiencia de Usuario: La accesibilidad no solo beneficia a las personas con discapacidades, sino que también mejora la experiencia de usuario para todos los usuarios, al hacer que la navegación sea más clara, consistente y fácil de usar.</p>
