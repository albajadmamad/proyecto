<h2>¿Qué es un array en PHP?</h2>
<br>
<p>
En PHP, un array es una estructura de datos que puede contener una colección ordenada de elementos. Cada elemento en un array está asociado con un índice que puede ser numérico o alfanumérico. Los arrays en PHP pueden contener diferentes tipos de datos, como números, cadenas, booleanos, otros arrays e incluso objetos.
</p>
<br>
<br>
<h2>¿Qué operaciones con arrays se pueden realizar para manipular y trabajar
datos?
</h2>
<p>Hay una variedad de operaciones que se pueden realizar con arrays en PHP para manipular y trabajar con datos. Algunas de las operaciones más comunes incluyen:

Agregar elementos: Se pueden agregar elementos a un array utilizando funciones como array_push(), array_unshift() o simplemente asignando un valor a un nuevo índice.

php

$array = array(1, 2, 3);
array_push($array, 4); // Agregar al final del array
array_unshift($array, 0); // Agregar al principio del array
$array[4] = 5; // Agregar un elemento con un índice específico
Eliminar elementos: Se pueden eliminar elementos de un array utilizando funciones como array_pop() para eliminar el último elemento, array_shift() para eliminar el primer elemento o unset() para eliminar un elemento específico por su índice.

php

$array = array(1, 2, 3, 4, 5);
array_pop($array); // Eliminar el último elemento
array_shift($array); // Eliminar el primer elemento
unset($array[2]); // Eliminar el elemento con índice 2</p>

